package com.think2exam.projectt2e.modals;

public class TPRModel {

    public String title;
    public String copyrightInfo;
    public String link;
    public String licAbs;
    public String licLink;
    public String licAl;

    public TPRModel(String title, String copyrightInfo, String link, String licAbs, String licLink, String licAl) {
        this.title = title;
        this.copyrightInfo = copyrightInfo;
        this.link = link;
        this.licAbs = licAbs;
        this.licLink = licLink;
        this.licAl = licAl;
    }
}
